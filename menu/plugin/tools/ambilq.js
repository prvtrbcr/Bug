const fs = require('fs');
const kripto = require('crypto');
require("../../../config");

module.exports = {
    type: 'tools',
    command: ['ambilq', 'getq'],
    operate: async (context) => {
        const { famofc, m, q, prefix, command, zreply } = context;
        if (!m.quoted) return zreply(`*Reply to the quoted message to be retrieved*`);
        
        let penis = JSON.stringify({ [m.quoted.mtype]: m.quoted }, null, 2);
        let jeneng = `MessageData_${kripto.randomBytes(8).toString('hex')}.json`;

        await fs.writeFileSync(jeneng, penis);
        await zreply(penis);
        await famofc.sendMessage(m.chat, { document: { url: `./${jeneng}` }, fileName: jeneng, mimetype: '*/*' }, { quoted: m });
        await fs.unlinkSync(jeneng);
    }
};